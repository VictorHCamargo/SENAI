// while
// while (condição) {
    // bloco de código
//}
let i = 9
while (i < 10) {
    console.log(i);
    i++;
}

let j = 10;
while (j > 0) {
    console.log(j)
    j--;
}

let t = 0;
while (t <= 100) {
    console.log(t);
    t += 10;
} 

